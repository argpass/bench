CREATE TABLE IF NOT EXISTS `t_location` (
  `id` bigint(20) unsigned NOT NULL AUTO_INCREMENT,
  `car_id` varchar(32) COLLATE utf8_bin DEFAULT NULL,
  `tid` varchar(32) COLLATE utf8_bin NOT NULL DEFAULT '' COMMENT '车辆终端序列号',
  `category` tinyint(3) NOT NULL DEFAULT '0' COMMENT '1: 移动点\r\n2: 停留点\r\n3: 实时位置更新告警(预留)\r\n4：震动告警(预留)\r\n5：低电告警\r\n6： 满电告警\r\n7： 断电告警\r\n8：低燃油告警(预留)\r\n9：高水温告警(预留)\r\n10：OBD断开告警(预留)\r\n11：电门锁开启(预留)\r\n12: 自检异常',
  `latitude` bigint(20) NOT NULL DEFAULT '0' COMMENT '纬度',
  `longitude` bigint(20) NOT NULL DEFAULT '0' COMMENT '经度',
  `clatitude` bigint(20) NOT NULL DEFAULT '0' COMMENT '加密后的纬度',
  `clongitude` bigint(20) NOT NULL DEFAULT '0' COMMENT '加密后的经度',
  `address` text COLLATE utf8_bin COMMENT '地点名称',
  `altitude` bigint(20) NOT NULL DEFAULT '0' COMMENT '高度',
  `speed` int(5) DEFAULT '0' COMMENT '当前时速度。km/h. int, 同 max_speed',
  `degree` float(10,2) DEFAULT '0.00' COMMENT '方位角',
  `locate_error` int(11) NOT NULL DEFAULT '2000' COMMENT '定位误差。单位：米\r\ngps；误差20米\r\n基站定位误差：2公里',
  `snr` int(11) unsigned NOT NULL DEFAULT '0' COMMENT 'GPS载噪比',
  `mcc` varchar(20) COLLATE utf8_bin DEFAULT NULL COMMENT '基站信息mcc',
  `mnc` varchar(20) COLLATE utf8_bin DEFAULT NULL COMMENT '基站信息mnc',
  `lac` varchar(30) COLLATE utf8_bin DEFAULT NULL COMMENT '基站信息lac',
  `cell_id` varchar(30) COLLATE utf8_bin DEFAULT NULL COMMENT '基站信息',
  `timestamp` bigint(20) NOT NULL DEFAULT '0' COMMENT 'GPS定位时间',
  `t_type` varchar(10) COLLATE utf8_bin DEFAULT NULL COMMENT '终端类型 ZJ210,ZJ211,ZJ300',
  `locate_type` int(1) DEFAULT '1' COMMENT '终端定位类型\r\n0:基站定位\r\n1:gps定位（卫星定位）\r\n2:wifi定位',
  `rxlev` int(10) DEFAULT NULL COMMENT '接收基站信号强度',
  PRIMARY KEY (`id`,`timestamp`,`category`),
  KEY `location_tid` (`tid`,`timestamp`,`category`) USING BTREE,
  KEY `loc1` (`car_id`,`timestamp`,`locate_type`,`category`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='位置信息。'
